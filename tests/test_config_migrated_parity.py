def test_registered_default_is_old_site_literal():
    for key, old_literal in MIGRATED_SITE_DEFAULTS.items():
        assert key in CONFIG_DEFAULTS, f"{key} missing from registry"
        assert CONFIG_DEFAULTS[key] == old_literal, f"registry default drifted from old literal for {key}"


def test_resolve_matches_getenv_for_migrated_keys(monkeypatch):
    for key, old_literal in MIGRATED_SITE_DEFAULTS.items():
        monkeypatch.delenv(key, raising=False)
        assert resolve(key) == os.getenv(key, old_literal), f"unset {key}"
        monkeypatch.setenv(key, f"OVR::{key}")
        assert resolve(key) == os.getenv(key, old_literal) == f"OVR::{key}", f"override {key}"

