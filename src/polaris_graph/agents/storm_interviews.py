async def run_storm_interviews(
    client: OpenRouterClient,
    state: ResearchState,
) -> dict:
    """Execute TRUE STORM multi-perspective research interviews.

    Runs between search and analyze nodes. Takes initial search results,
    conducts simulated expert interviews with diverse personas, and
    produces enriched evidence plus a hierarchical outline.

    The function is idempotent with respect to state — it reads from state
    and returns a dict of state updates (never mutates state directly).

    Args:
        client: OpenRouter LLM client for all LLM calls.
        state: Current ResearchState with search results.

    Returns:
        Dict with state updates:
        - storm_conversations: List of serialized StormConversation dicts.
        - storm_outline: List of serialized StormOutlineSection dicts.
        - web_results: Original + interview-sourced web results.
        - academic_results: Original academic results (pass-through).
    """
    if not PG_STORM_ENABLED:
        logger.info("[STORM] STORM interviews disabled (PG_STORM_ENABLED=0)")
        return {}

    tracer = get_tracer()
    start_time = time.monotonic()
    original_query = state["original_query"]
    region = state["region"]
    web_results = list(state.get("web_results", []))
    academic_results = list(state.get("academic_results", []))

    logger.info(
        "[STORM] Starting STORM interviews for: %s",
        original_query[:100],
    )
    logger.info(
        "[STORM] Config: %d perspectives, %d rounds/perspective, %ds budget",
        PG_STORM_PERSPECTIVES_COUNT,
        PG_STORM_ROUNDS_PER_PERSPECTIVE,
        PG_STORM_MAX_TIME_SECONDS,
    )

    if tracer:
        tracer.node_start(
            "storm_interviews",
            perspectives=PG_STORM_PERSPECTIVES_COUNT,
            rounds_per_perspective=PG_STORM_ROUNDS_PER_PERSPECTIVE,
            time_budget=PG_STORM_MAX_TIME_SECONDS,
            existing_web_results=len(web_results),
            existing_academic_results=len(academic_results),
        )

    # -----------------------------------------------------------------------
    # Phase 1: Dynamic Perspective Discovery
    # -----------------------------------------------------------------------
    logger.info("[STORM] === Phase 1: Perspective Discovery ===")

    # Build context from existing search results for grounding
    existing_context_lines = []
    for r in (web_results + academic_results)[:20]:
        title = r.get("title", "")
        snippet = r.get("snippet", "")[:200]
        if title:
            existing_context_lines.append(f"- {title}: {snippet}")

    existing_context = "\n".join(existing_context_lines) if existing_context_lines else "(no prior results)"

    personas = await _discover_perspectives(
        client=client,
        query=original_query,
        existing_context=existing_context,
        target_count=PG_STORM_PERSPECTIVES_COUNT,
    )

    if not personas:
        logger.error("[STORM] Phase 1: No personas generated — aborting STORM interviews")
        if tracer:
            tracer.node_end("storm_interviews", result="failed", reason="no_personas")
        return {}

    phase_1_elapsed = time.monotonic() - start_time
    logger.info(
        "[STORM] Phase 1 complete: %d personas in %.1fs",
        len(personas),
        phase_1_elapsed,
    )

    if tracer:
        tracer.llm_call(
            "storm_interviews",
            "perspective_discovery",
            personas_generated=len(personas),
            perspectives=[p.perspective for p in personas],
        )

    # -----------------------------------------------------------------------
    # Phase 2: Multi-Round Interview Simulation
    # -----------------------------------------------------------------------
    logger.info("[STORM] === Phase 2: Interview Simulation ===")

    all_conversations: list[StormConversation] = []
    all_interview_results: list[dict] = []
    completed_perspectives: list[str] = []
    skipped_perspectives: list[str] = []

    # FIX-PARALLEL: Run STORM interviews concurrently with semaphore control.
    # Pre-allocate time budget per persona (total budget / persona count).
    # Previous: sequential loop meant later personas got exponentially less time.
    storm_concurrency = int(os.getenv("PG_STORM_CONCURRENCY", "4"))
    storm_sem = asyncio.Semaphore(storm_concurrency)
    per_persona_budget = max(
        60.0,
        (PG_STORM_MAX_TIME_SECONDS - (time.monotonic() - start_time)) / max(len(personas), 1),
    )

    logger.info(
        "[STORM] FIX-PARALLEL: Running %d interviews with concurrency=%d, "
        "%.0fs budget per persona",
        len(personas),
        storm_concurrency,
        per_persona_budget,
    )

    async def _bounded_interview(persona_idx: int, persona: "StormPersona"):
        """Run a single interview bounded by semaphore and time budget."""
        async with storm_sem:
            # Check if global budget is exhausted before starting
            elapsed = time.monotonic() - start_time
            remaining = PG_STORM_MAX_TIME_SECONDS - elapsed
            if remaining <= 30.0:
                logger.warning(
                    "[STORM] Global budget nearly exhausted (%.0fs) — "
                    "skipping %s",
                    remaining,
                    persona.name,
                )
                return persona_idx, None, [], persona.perspective

            logger.info(
                "[STORM] Interview %d/%d: %s (%s) — %.0fs global remaining, "
                "%.0fs budget",
                persona_idx + 1,
                len(personas),
                persona.name,
                persona.perspective,
                remaining,
                per_persona_budget,
            )

            # FIX-058F: Per-interview timeout to prevent hung interviews blocking pipeline
            interview_timeout = int(os.getenv("PG_STORM_INTERVIEW_TIMEOUT", "300"))
            try:
                conversation, interview_results = await asyncio.wait_for(
                    _conduct_interview(
                        client=client,
                        persona=persona,
                        query=original_query,
                        region=region,
                        max_rounds=PG_STORM_ROUNDS_PER_PERSPECTIVE,
                        time_budget_remaining=per_persona_budget,
                    ),
                    timeout=interview_timeout,
                )
                return persona_idx, conversation, interview_results, persona.perspective
            except asyncio.TimeoutError:
                logger.warning(
                    "[STORM] FIX-058F: Interview timed out after %ds for %s (%s) — skipping",
                    interview_timeout,
                    persona.name,
                    persona.perspective,
                )
                # WAVE-4.5: Trace interview timeout
                _to_tracer = get_tracer()
                if _to_tracer:
                    _to_tracer.llm_call("storm_interviews", "interview_failed",
                        persona_name=persona.name, perspective=persona.perspective,
                        persona_index=persona_idx, failure="timeout",
                        timeout_seconds=interview_timeout)
                return persona_idx, None, [], persona.perspective
            except Exception as exc:
                logger.warning(
                    "[STORM] Interview failed for %s (%s): %s — skipping",
                    persona.name,
                    persona.perspective,
                    str(exc)[:200],
                )
                # WAVE-4.5: Trace interview exception
                _ex_tracer = get_tracer()
                if _ex_tracer:
                    _ex_tracer.llm_call("storm_interviews", "interview_failed",
                        persona_name=persona.name, perspective=persona.perspective,
                        persona_index=persona_idx, failure="exception",
                        error=str(exc)[:200])
                return persona_idx, None, [], persona.perspective

    # Launch all interviews
    interview_tasks = [
        _bounded_interview(idx, persona)
        for idx, persona in enumerate(personas)
    ]
    interview_results_raw = await asyncio.gather(*interview_tasks, return_exceptions=True)

    # Collect results in original order
    for result in interview_results_raw:
        if isinstance(result, Exception):
            logger.warning(
                "[STORM] Interview task failed: %s",
                str(result)[:200],
            )
            continue

        persona_idx, conversation, interview_results, perspective = result
        if conversation and conversation.rounds:
            all_conversations.append(conversation)
            all_interview_results.extend(interview_results)
            completed_perspectives.append(perspective)
        else:
            skipped_perspectives.append(perspective)

    phase_2_elapsed = time.monotonic() - start_time
    total_rounds = sum(len(c.rounds) for c in all_conversations)
    total_interview_search_results = len(all_interview_results)

    logger.info(
        "[STORM] Phase 2 complete: %d conversations, %d total rounds, "
        "%d search results in %.1fs",
        len(all_conversations),
        total_rounds,
        total_interview_search_results,
        phase_2_elapsed,
    )

    if skipped_perspectives:
        logger.warning(
            "[STORM] Skipped perspectives: %s",
            ", ".join(skipped_perspectives),
        )

    if tracer:
        tracer.llm_call(
            "storm_interviews",
            "interview_simulation",
            conversations=len(all_conversations),
            total_rounds=total_rounds,
            search_results=total_interview_search_results,
            completed_perspectives=completed_perspectives,
            skipped_perspectives=skipped_perspectives,
        )

    if not all_conversations:
        logger.error("[STORM] Phase 2: No conversations completed — aborting outline")
        if tracer:
            tracer.node_end("storm_interviews", result="failed", reason="no_conversations")
        return {}

    # -----------------------------------------------------------------------
    # Phase 3: Outline Generation from Conversations
    # -----------------------------------------------------------------------

    # Time budget check before Phase 3
    elapsed = time.monotonic() - start_time
    remaining = PG_STORM_MAX_TIME_SECONDS - elapsed

    if remaining <= 15.0:
        logger.warning(
            "[STORM] Time budget too low for outline generation (%.0fs remaining) "
            "— using fallback outline",
            remaining,
        )
        storm_outline = _fallback_outline(all_conversations, original_query)
    else:
        logger.info("[STORM] === Phase 3: Outline Generation ===")
        storm_outline = await _generate_outline_from_conversations(
            client=client,
            query=original_query,
            conversations=all_conversations,
        )

    phase_3_elapsed = time.monotonic() - start_time

    logger.info(
        "[STORM] Phase 3 complete: %d outline sections in %.1fs total",
        len(storm_outline),
        phase_3_elapsed,
    )

    if tracer:
        tracer.llm_call(
            "storm_interviews",
            "outline_generation",
            sections=len(storm_outline),
        )

    # -----------------------------------------------------------------------
    # Assemble state update
    # -----------------------------------------------------------------------

    # Merge interview search results into web_results (they are web searches)
    # Deduplicate by URL before merging
    existing_urls = {r.get("url", "") for r in web_results if r.get("url")}
    new_results = [
        r for r in all_interview_results
        if r.get("url", "") and r.get("url", "") not in existing_urls
    ]
    enriched_web_results = web_results + new_results

    # Serialize conversations
    serialized_conversations = [c.model_dump() for c in all_conversations]

    total_elapsed = time.monotonic() - start_time
    total_llm_calls = (
        1  # Phase 1: persona discovery
        + total_rounds * 2  # Phase 2: question + answer per round
        + 1  # Phase 3: outline generation
    )

    logger.info(
        "[STORM] === STORM Interviews Complete ===\n"
        "  Perspectives: %d completed, %d skipped\n"
        "  Conversations: %d with %d total rounds\n"
        "  New search results: %d (total web: %d)\n"
        "  Outline sections: %d\n"
        "  LLM calls: ~%d\n"
        "  Total time: %.1fs",
        len(completed_perspectives),
        len(skipped_perspectives),
        len(all_conversations),
        total_rounds,
        len(new_results),
        len(enriched_web_results),
        len(storm_outline),
        total_llm_calls,
        total_elapsed,
    )

    if tracer:
        tracer.node_end(
            "storm_interviews",
            result="success",
            conversations=len(all_conversations),
            total_rounds=total_rounds,
            new_search_results=len(new_results),
            outline_sections=len(storm_outline),
            llm_calls=total_llm_calls,
            elapsed_seconds=round(total_elapsed, 1),
            completed_perspectives=completed_perspectives,
            skipped_perspectives=skipped_perspectives,
        )

    return {
        "storm_conversations": serialized_conversations,
        "storm_outline": storm_outline,
        "web_results": enriched_web_results,
        "academic_results": academic_results,
    }
