async def _discover_perspectives(
    client: OpenRouterClient,
    query: str,
    existing_context: str,
    target_count: int,
) -> list[StormPersona]:
    """Generate diverse research personas based on the topic.

    Uses the base STORM_PERSPECTIVES as guidance but generates unique
    persona descriptions with specific expertise relevant to the query.
    Post-processes with _dedup_personas to eliminate >70% word overlap.

    Args:
        client: OpenRouter LLM client.
        query: The original research question.
        existing_context: Snippets from initial search results for grounding.
        target_count: Number of personas to generate.

    Returns:
        List of StormPersona objects, one per perspective.
    """
    # Select perspectives (cap to target_count)
    perspectives_to_use = STORM_PERSPECTIVES[:target_count]

    perspective_descriptions = "\n".join(
        f"  {i+1}. {p}" for i, p in enumerate(perspectives_to_use)
    )

    prompt = (
        f"You are designing a multi-perspective research team to investigate "
        f"a complex research question through simulated expert interviews.\n\n"
        f"RESEARCH QUESTION: {query}\n\n"
        f"INITIAL RESEARCH CONTEXT (from preliminary search):\n{existing_context}\n\n"
        f"Create exactly {target_count} expert personas, one for each of these "
        f"research perspectives:\n{perspective_descriptions}\n\n"
        f"DIVERSITY REQUIREMENTS (CRITICAL):\n"
        f"- Each persona MUST have a DIFFERENT primary discipline. Do NOT repeat "
        f"disciplines across personas.\n"
        f"- Ensure at least one persona from each of: hard science (physics, "
        f"chemistry, biology, engineering), social science (sociology, psychology, "
        f"anthropology), economics/finance, policy/regulation, industry/practice, "
        f"and public health — as applicable to the research question.\n"
        f"- Each expertise description MUST use DIFFERENT technical vocabulary. "
        f"If two personas share >50% of their terminology, they are NOT distinct "
        f"enough.\n"
        f"- Vary methodological approaches: quantitative vs qualitative, "
        f"experimental vs observational, micro vs macro, theoretical vs applied.\n\n"
        f"For each persona, provide:\n"
        f"- perspective: The exact perspective name from the list above\n"
        f"- name: A realistic expert name (e.g., 'Dr. Maria Rodriguez')\n"
        f"- expertise: 2-3 sentences describing their specific domain expertise, "
        f"academic background, and why their viewpoint matters for this topic. "
        f"Make it specific to the research question, not generic.\n"
        f"- question_focus: What specific aspect of the research question this "
        f"expert would focus on. Be concrete — cite specific subtopics, "
        f"methodologies, or data types they would look for.\n\n"
        f"Make each persona distinct. Their expertise should NOT overlap. "
        f"Together they should cover the full landscape of the research question.\n\n"
        f"Return a JSON object with a 'personas' array."
    )

    try:
        result = await client.generate_structured(
            prompt=prompt,
            schema=StormPersonaBatch,
            max_tokens=PG_STORM_PERSONA_MAX_TOKENS,
            reasoning_enabled=False,
        )

        if result and result.personas:
            logger.info(
                "[STORM] Phase 1: Generated %d personas for %d perspectives",
                len(result.personas),
                target_count,
            )
            for persona in result.personas:
                logger.debug(
                    "[STORM]   %s (%s): %s",
                    persona.name,
                    persona.perspective,
                    persona.question_focus[:80],
                )
            return _dedup_personas(result.personas, query)

    except Exception as exc:
        logger.warning(
            "[STORM] Phase 1: Persona generation failed: %s — using fallback personas",
            str(exc)[:200],
        )

    # Fallback: generate basic personas without LLM
    return _fallback_personas(perspectives_to_use, query)


def _dedup_personas(
    personas: list[StormPersona],
    query: str,
) -> list[StormPersona]:
    """Remove personas with >70% word overlap in expertise fields.

    Compares each pair of personas by tokenizing their expertise description
    into word sets and computing overlap ratio against the smaller set.
    If overlap exceeds the threshold, the second persona is dropped and a
    warning is logged.

    Args:
        personas: List of generated personas.
        query: Original research question (for logging context).

    Returns:
        Deduplicated list of personas.
    """
    if len(personas) <= 1:
        return personas

    dedup_threshold = float(os.getenv("PG_STORM_PERSONA_DEDUP_THRESHOLD", "0.70"))
    deduped: list[StormPersona] = []
    dropped_count = 0

    for persona in personas:
        words_new = set(persona.expertise.lower().split())
        is_duplicate = False

        for existing in deduped:
            words_existing = set(existing.expertise.lower().split())
            if not words_new or not words_existing:
                continue
            intersection = words_new & words_existing
            smaller_set_size = min(len(words_new), len(words_existing))
            if smaller_set_size == 0:
                continue
            overlap = len(intersection) / smaller_set_size
            if overlap > dedup_threshold:
                logger.warning(
                    "[STORM] Persona dedup: dropping '%s' (%s) — %.0f%% word "
                    "overlap with '%s' (%s) in expertise field",
                    persona.name,
                    persona.perspective,
                    overlap * 100,
                    existing.name,
                    existing.perspective,
                )
                is_duplicate = True
                dropped_count += 1
                break

        if not is_duplicate:
            deduped.append(persona)

    if dropped_count > 0:
        logger.info(
            "[STORM] Persona dedup: kept %d/%d personas (dropped %d with >%.0f%% overlap) "
            "for query: %s",
            len(deduped),
            len(personas),
            dropped_count,
            dedup_threshold * 100,
            query[:80],
        )

    return deduped


def _fallback_personas(
    perspectives: list[str],
    query: str,
) -> list[StormPersona]:
    """Generate fallback personas when LLM fails.

    Creates template-based personas that are functional but less nuanced
    than LLM-generated ones.
    """
    fallback_names = {
        "Scientific": "Dr. Sarah Chen",
        "Regulatory": "James Morrison, JD",
        "Industry": "Patricia Nakamura",
        "Economic": "Dr. Robert Okafor",
        "Public_Health": "Dr. Maria Gonzalez",
        "Historical": "Prof. David Whitfield",
        "Regional": "Anika Patel",
        "Methodological": "Dr. Kenji Tanaka",
        "Emerging_Trends": "Dr. Elisa Ferreira",
    }

    fallback_expertise = {
        "Scientific": "peer-reviewed research methodology and experimental design",
        "Regulatory": "regulatory frameworks, compliance standards, and policy analysis",
        "Industry": "market applications, commercial viability, and industry adoption",
        "Economic": "cost-benefit analysis, market dynamics, and economic impact",
        "Public_Health": "population health outcomes, epidemiology, and health equity",
        "Historical": "historical development, evolution of practices, and precedent analysis",
        "Regional": "geographic variations, regional implementations, and local context",
        "Methodological": "research methods, measurement techniques, and analytical frameworks",
        "Emerging_Trends": "frontier developments, emerging technologies, and future directions",
    }

    personas = []
    for perspective in perspectives:
        name = fallback_names.get(perspective, f"Expert ({perspective})")
        expertise = fallback_expertise.get(perspective, f"expertise in {perspective.lower()}")

        personas.append(StormPersona(
            perspective=perspective,
            name=name,
            expertise=f"Specialist in {expertise} with focus on topics related to: {query[:100]}",
            question_focus=f"Investigating the {perspective.lower().replace('_', ' ')} "
                           f"dimensions of the research question.",
        ))

    logger.info(
        "[STORM] Phase 1 fallback: Generated %d template personas",
        len(personas),
    )
    return personas


