async def _generate_question(
    client: OpenRouterClient,
    persona: StormPersona,
    query: str,
    conversation_history: list[dict],
    round_number: int,
) -> Optional[StormQuestion]:
    """Generate a targeted question from a persona's perspective.

    Uses the persona's expertise and prior conversation rounds to generate
    a progressively deeper question. Round 1 is broad; later rounds drill
    into gaps identified in previous answers.

    Args:
        client: OpenRouter LLM client.
        persona: The interviewing persona.
        query: Original research question.
        conversation_history: Previous rounds of {question, answer} dicts.
        round_number: Current round (1-indexed).

    Returns:
        StormQuestion with the question and decomposed search queries,
        or None if generation fails.
    """
    history_context = ""
    if conversation_history:
        history_lines = []
        for i, rnd in enumerate(conversation_history, 1):
            history_lines.append(
                f"Round {i}:\n"
                f"  Q: {rnd.get('question', '')[:200]}\n"
                f"  A: {rnd.get('answer', '')[:300]}"
            )
        history_context = (
            f"\nPREVIOUS CONVERSATION ROUNDS:\n"
            + "\n".join(history_lines)
            + "\n"
        )

    round_guidance = ""
    if round_number == 1:
        round_guidance = (
            "This is the FIRST question. Ask a broad, foundational question "
            "that establishes the key facts and current state of knowledge "
            "from your perspective."
        )
    elif round_number == 2:
        round_guidance = (
            "This is the SECOND question. Based on the first answer, identify "
            "a specific gap or surprising finding and drill deeper into it."
        )
    elif round_number == 3:
        round_guidance = (
            "This is the THIRD question. Look for contradictions, nuances, "
            "or under-explored aspects. Ask about methodology, data quality, "
            "or alternative interpretations."
        )
    else:
        round_guidance = (
            f"This is round {round_number}. Ask about the most important "
            f"remaining gap in your understanding. Focus on what is still "
            f"UNKNOWN or CONTESTED, not what has already been well-answered."
        )

    prompt = (
        f"You are {persona.name}, an expert in {persona.expertise}.\n\n"
        f"You are conducting a research interview about:\n"
        f"RESEARCH QUESTION: {query}\n\n"
        f"Your perspective: {persona.perspective}\n"
        f"Your focus area: {persona.question_focus}\n"
        f"{history_context}\n"
        f"{round_guidance}\n\n"
        f"Generate:\n"
        f"1. ONE focused, specific research question from your expert perspective. "
        f"Do NOT repeat questions already asked. The question should be answerable "
        f"through web/academic search — avoid purely philosophical or speculative questions.\n"
        f"2. Exactly {PG_STORM_SEARCH_QUERIES_PER_QUESTION} web search queries that "
        f"would help find the answer. Use precise, search-engine-friendly language. "
        f"Include at least one query with specific technical terms or named entities.\n\n"
        f"Return a JSON object with 'question', 'search_queries', and 'perspective' fields."
    )

    try:
        result = await client.generate_structured(
            prompt=prompt,
            schema=StormQuestion,
            max_tokens=PG_STORM_QUESTIONS_MAX_TOKENS,
            reasoning_enabled=False,
        )

        if result and result.question:
            # Enforce search query count limit
            result.search_queries = result.search_queries[:PG_STORM_SEARCH_QUERIES_PER_QUESTION]
            logger.debug(
                "[STORM] %s (round %d) asks: %s [%d queries]",
                persona.name,
                round_number,
                result.question[:100],
                len(result.search_queries),
            )
            return result

    except Exception as exc:
        logger.warning(
            "[STORM] Question generation failed for %s round %d: %s",
            persona.name,
            round_number,
            str(exc)[:200],
        )

    return None


async def _synthesize_answer(
    client: OpenRouterClient,
    persona: StormPersona,
    question: str,
    search_results: list[dict],
    query: str,
) -> Optional[StormAnswer]:
    """Synthesize an expert answer grounded in search results.

    The LLM acts as a knowledgeable expert responding to the persona's
    question, but ONLY using information from the provided search results.
    No hallucination allowed.

    Args:
        client: OpenRouter LLM client.
        persona: The persona who asked the question (for context).
        question: The question to answer.
        search_results: Web search results to ground the answer in.
        query: Original research question for relevance context.

    Returns:
        StormAnswer with synthesized answer and key findings,
        or None if synthesis fails.
    """
    # Build search context from results
    context_lines = []
    source_urls = []
    for i, r in enumerate(search_results[:15], 1):
        title = r.get("title", "Untitled")
        snippet = r.get("snippet", "")[:400]
        url = r.get("url", "")
        source_urls.append(url)
        context_lines.append(
            f"[Source {i}] {title}\n"
            f"  URL: {url}\n"
            f"  Content: {snippet}"
        )

    if not context_lines:
        logger.warning(
            "[STORM] No search results to ground answer for: %s",
            question[:80],
        )
        return None

    search_context = "\n".join(context_lines)

    prompt = (
        f"You are a research expert being interviewed about:\n"
        f"RESEARCH QUESTION: {query}\n\n"
        f"The interviewer ({persona.name}, {persona.perspective} perspective) asks:\n"
        f"QUESTION: {question}\n\n"
        f"SEARCH RESULTS (your ONLY source of information):\n{search_context}\n\n"
        f"INSTRUCTIONS:\n"
        f"1. Provide a comprehensive, well-structured answer (200-400 words).\n"
        f"2. ONLY use information from the search results above. Do NOT add "
        f"information not present in the sources.\n"
        f"3. When citing specific facts, mention which source they come from.\n"
        f"4. Extract 3-5 key findings as bullet points.\n"
        f"5. List the URLs of sources you actually used in your answer.\n"
        f"6. If the search results do not adequately answer the question, "
        f"say so explicitly and note what information is missing.\n\n"
        f"Return a JSON object with 'answer', 'key_findings', and 'sources_used' fields."
    )

    try:
        result = await client.generate_structured(
            prompt=prompt,
            schema=StormAnswer,
            max_tokens=PG_STORM_ANSWER_MAX_TOKENS,
            reasoning_enabled=False,
        )

        if result and result.answer:
            logger.debug(
                "[STORM] Answer synthesized: %d chars, %d findings, %d sources",
                len(result.answer),
                len(result.key_findings),
                len(result.sources_used),
            )
            return result

    except Exception as exc:
        logger.warning(
            "[STORM] Answer synthesis failed for question '%s': %s",
            question[:60],
            str(exc)[:200],
        )

    return None


async def _conduct_interview(
    client: OpenRouterClient,
    persona: StormPersona,
    query: str,
    region: str,
    max_rounds: int,
    time_budget_remaining: float,
) -> tuple[StormConversation, list[dict]]:
    """Conduct a multi-round interview with a single persona.

    Each round: persona asks -> search -> expert answers.
    Accumulates conversation history and all search results.

    Args:
        client: OpenRouter LLM client.
        persona: The interviewing persona.
        query: Original research question.
        region: Search region.
        max_rounds: Maximum interview rounds.
        time_budget_remaining: Seconds remaining in total STORM budget.

    Returns:
        Tuple of (StormConversation, list of all search results from this interview).
    """
    conversation = StormConversation(
        perspective=persona.perspective,
        persona_name=persona.name,
        rounds=[],
    )
    all_search_results: list[dict] = []
    interview_start = time.monotonic()

    # FIX-1: Use the caller's fair-shared budget directly (no further halving).
    # The caller already computed: remaining / perspectives_remaining.
    per_interview_budget = time_budget_remaining

    for round_num in range(1, max_rounds + 1):
        elapsed = time.monotonic() - interview_start
        if elapsed > per_interview_budget:
            logger.info(
                "[STORM] %s interview: time budget exhausted at round %d/%.0fs",
                persona.name,
                round_num,
                elapsed,
            )
            break

        # Step 1: Generate question
        question_obj = await _generate_question(
            client=client,
            persona=persona,
            query=query,
            conversation_history=conversation.rounds,
            round_number=round_num,
        )

        if not question_obj or not question_obj.question:
            logger.warning(
                "[STORM] %s round %d: question generation failed, stopping interview",
                persona.name,
                round_num,
            )
            break

        # Step 2: Execute search with decomposed queries
        # WARN-1 FIX: Pass perspective so results carry perspective_source tag
        round_results = await _search_for_question(
            question_queries=question_obj.search_queries,
            region=region,
            perspective=persona.perspective,
        )
        all_search_results.extend(round_results)

        logger.info(
            "[STORM] %s round %d: searched %d queries -> %d results",
            persona.name,
            round_num,
            len(question_obj.search_queries),
            len(round_results),
        )

        # Step 3: Synthesize answer grounded in search results
        answer_obj = await _synthesize_answer(
            client=client,
            persona=persona,
            question=question_obj.question,
            search_results=round_results,
            query=query,
        )

        if not answer_obj:
            logger.warning(
                "[STORM] %s round %d: answer synthesis failed, recording question only",
                persona.name,
                round_num,
            )
            # Record the question even if answer failed (partial data)
            conversation.rounds.append({
                "question": question_obj.question,
                "answer": "[Answer synthesis failed — no search results or LLM error]",
                "sources": [],
                "key_findings": [],
            })
            continue

        # FIX-P2: Detect STORM interview failures — answers that explicitly state
        # sources are inadequate should not pollute the evidence pool.
        _failure_patterns = (
            "do not adequately",
            "does not adequately",
            "do NOT",
            "data is incomplete",
            "not directly reported",
            "none of the sources directly",
            "sources do not provide",
            "no specific data",
            "insufficient information",
            "cannot be determined from",
        )
        _answer_lower = answer_obj.answer.lower() if answer_obj.answer else ""
        _is_failed_interview = any(fp.lower() in _answer_lower for fp in _failure_patterns)

        # Record the round (with failure flag)
        conversation.rounds.append({
            "question": question_obj.question,
            "answer": answer_obj.answer,
            "sources": answer_obj.sources_used,
            "key_findings": answer_obj.key_findings if not _is_failed_interview else [],
            "interview_quality": "failed" if _is_failed_interview else "ok",
        })
        if _is_failed_interview:
            logger.info(
                "[STORM] FIX-P2: %s round %d marked FAILED — answer indicates "
                "inadequate sources. key_findings cleared.",
                persona.name, round_num,
            )

        # OBS-STORM: Emit full Q&A to trace
        _storm_tracer = get_tracer()
        if _storm_tracer:
            _storm_tracer.storm_transcript(
                persona=persona.name,
                round_num=round_num,
                question=question_obj.question,
                answer=answer_obj.answer,
                sources=answer_obj.sources_used,
                key_findings=answer_obj.key_findings if not _is_failed_interview else [],
                expertise=persona.expertise[:200],
                question_focus=persona.question_focus[:200],
                interview_quality="failed" if _is_failed_interview else "ok",
            )

    logger.info(
        "[STORM] %s interview complete: %d rounds, %d total search results",
        persona.name,
        len(conversation.rounds),
        len(all_search_results),
    )

    # WAVE-4.5: Per-persona interview complete trace
    _storm_tracer = get_tracer()
    if _storm_tracer:
        _storm_tracer.llm_call("storm_interviews", "interview_complete",
            persona_name=persona.name,
            perspective=persona.perspective,
            rounds_completed=len(conversation.rounds),
            search_results=len(all_search_results),
            qa_pairs=len(conversation.rounds))

    return conversation, all_search_results


