class StormPersona(BaseModel):
    """A research persona with an assigned perspective."""

    perspective: str = Field(description="STORM perspective name")
    name: str = Field(description="Persona name, e.g. 'Dr. Sarah Chen'")
    expertise: str = Field(
        description="Description of the persona's expertise and background"
    )
    question_focus: str = Field(
        description="What this persona is most interested in exploring"
    )

    @model_validator(mode="before")
    @classmethod
    def normalize_field_names(cls, data):
        """Handle LLM variant field names and nulls."""
        if isinstance(data, dict):
            for alt in ("expert_name", "persona_name", "full_name"):
                if alt in data and "name" not in data:
                    data["name"] = data.pop(alt)
            for alt in ("background", "expert_background", "expertise_description"):
                if alt in data and "expertise" not in data:
                    data["expertise"] = data.pop(alt)
            for alt in ("focus", "research_focus", "primary_interest", "focus_area"):
                if alt in data and "question_focus" not in data:
                    data["question_focus"] = data.pop(alt)
            _null_defaults = {
                "perspective": "Scientific",
                "name": "Expert",
                "expertise": "",
                "question_focus": "",
            }
            for field_name, default in _null_defaults.items():
                if data.get(field_name) is None:
                    data[field_name] = default
        return data


class StormPersonaBatch(BaseModel):
    """Batch of STORM personas generated by LLM."""

    personas: list[StormPersona] = Field(
        description="List of research personas, one per perspective",
        default_factory=list,
    )

    @model_validator(mode="before")
    @classmethod
    def normalize_field_names(cls, data):
        """Handle LLM variant names."""
        if isinstance(data, dict):
            for alt in ("experts", "perspectives", "persona_list", "characters"):
                if alt in data and "personas" not in data:
                    data["personas"] = data.pop(alt)
            if data.get("personas") is None:
                data["personas"] = []
        return data

    @model_validator(mode="before")
    @classmethod
    def filter_invalid_personas(cls, data):
        """Skip personas that fail validation."""
        if isinstance(data, dict) and "personas" in data and isinstance(data["personas"], list):
            valid = []
            for item in data["personas"]:
                if not isinstance(item, dict):
                    continue
                try:
                    StormPersona.model_validate(item)
                    valid.append(item)
                except Exception as exc:
                    logger.warning(
                        "[STORM] Dropped invalid persona '%s': %s",
                        item.get("name", "?")[:40],
                        str(exc)[:200],
                    )
            data["personas"] = valid
        return data


