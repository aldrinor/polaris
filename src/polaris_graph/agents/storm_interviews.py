class StormOutlineSection(BaseModel):
    """A section in the STORM-generated outline."""

    title: str = Field(description="Section title")
    description: str = Field(description="What this section covers")
    evidence_summary: str = Field(
        description="Summary of evidence from conversations for this section",
        default="",
    )
    perspectives: list[str] = Field(
        description="STORM perspectives that contributed to this section",
        default_factory=list,
    )
    order: int = Field(description="Order in the outline", default=0)

    @field_validator("evidence_summary", mode="before")
    @classmethod
    def coerce_evidence_summary(cls, v):
        """RC-7: Belt-and-suspenders coercion for evidence_summary.

        LLM sometimes returns list/dict despite prompt saying string.
        normalize_field_names handles dict-level, this handles Pydantic-level.
        """
        if v is None:
            return ""
        if isinstance(v, list):
            return "; ".join(str(item)[:200] for item in v if item)
        if isinstance(v, dict):
            return "; ".join(f"{k}: {val}" for k, val in v.items())[:1000]
        if not isinstance(v, str):
            return str(v)[:1000]
        return v

    @model_validator(mode="before")
    @classmethod
    def normalize_field_names(cls, data):
        """Handle LLM variant names."""
        if isinstance(data, dict):
            for alt in ("section_title", "heading", "name"):
                if alt in data and "title" not in data:
                    data["title"] = data.pop(alt)
            for alt in ("content_description", "section_description", "summary"):
                if alt in data and "description" not in data:
                    data["description"] = data.pop(alt)
            for alt in ("evidence", "supporting_evidence", "evidence_from_conversations"):
                if alt in data and "evidence_summary" not in data:
                    data["evidence_summary"] = data.pop(alt)
            for alt in ("covered_perspectives", "perspective_list", "storm_perspectives"):
                if alt in data and "perspectives" not in data:
                    data["perspectives"] = data.pop(alt)
            for alt in ("section_order", "position", "index"):
                if alt in data and "order" not in data:
                    data["order"] = data.pop(alt)
            if data.get("title") is None:
                data["title"] = ""
            if data.get("description") is None:
                data["description"] = ""
            # FIX-STORM-TYPE: Coerce list/dict to string
            ev_summary = data.get("evidence_summary")
            if ev_summary is None:
                data["evidence_summary"] = ""
            elif isinstance(ev_summary, list):
                data["evidence_summary"] = "; ".join(
                    str(item)[:200] for item in ev_summary if item
                )
            elif isinstance(ev_summary, dict):
                data["evidence_summary"] = "; ".join(
                    f"{k}: {v}" for k, v in ev_summary.items()
                )[:1000]
            elif not isinstance(ev_summary, str):
                data["evidence_summary"] = str(ev_summary)[:1000]
            if data.get("perspectives") is None:
                data["perspectives"] = []
            if data.get("order") is None:
                data["order"] = 0
        return data


