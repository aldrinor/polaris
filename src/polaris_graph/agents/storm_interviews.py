async def _generate_outline_from_conversations(
    client: OpenRouterClient,
    query: str,
    conversations: list[StormConversation],
) -> list[dict]:
    """Generate a hierarchical outline from STORM conversation clusters.

    Analyzes all Q&A pairs across all perspectives, identifies thematic
    clusters, and produces a structured outline where each section inherits
    evidence from its contributing conversations.

    This is the ONE call that uses reasoning_enabled=True because it requires
    structural thinking to organize diverse conversations into a coherent outline.

    Args:
        client: OpenRouter LLM client.
        query: Original research question.
        conversations: All completed STORM conversations.

    Returns:
        List of outline section dicts (serialized StormOutlineSection).
    """
    if not conversations:
        logger.warning("[STORM] Phase 3: No conversations to generate outline from")
        return []

    # Build conversation digest for the LLM
    digest_lines = []
    total_rounds = 0
    for conv in conversations:
        digest_lines.append(
            f"\n=== {conv.perspective} ({conv.persona_name}) ==="
        )
        for rnd in conv.rounds:
            total_rounds += 1
            question = rnd.get("question", "")[:200]
            answer = rnd.get("answer", "")[:500]
            findings = rnd.get("key_findings", [])
            findings_text = "; ".join(f[:100] for f in findings[:5])
            digest_lines.append(
                f"  Q: {question}\n"
                f"  A: {answer}\n"
                f"  Key findings: {findings_text}"
            )

    conversation_digest = "\n".join(digest_lines)

    # Truncate digest if too long (stay within context window)
    max_digest_chars = 80000  # ~25K tokens, safe for Kimi K2.5 128K context
    if len(conversation_digest) > max_digest_chars:
        conversation_digest = conversation_digest[:max_digest_chars] + "\n[...truncated]"

    perspectives_used = list({conv.perspective for conv in conversations})

    prompt = (
        f"You are a research outline architect. Based on multi-perspective "
        f"expert interviews, create a comprehensive report outline.\n\n"
        f"RESEARCH QUESTION: {query}\n\n"
        f"INTERVIEW CONVERSATIONS ({len(conversations)} perspectives, "
        f"{total_rounds} total Q&A rounds):\n{conversation_digest}\n\n"
        f"PERSPECTIVES COVERED: {', '.join(perspectives_used)}\n\n"
        f"INSTRUCTIONS:\n"
        f"1. Identify 8-15 thematic clusters across ALL conversations.\n"
        f"2. For each cluster, create a report section with:\n"
        f"   - title: Clear, descriptive section heading\n"
        f"   - description: 2-3 sentences on what this section should cover\n"
        f"   - evidence_summary: Key findings from conversations that belong here\n"
        f"   - perspectives: Which STORM perspectives contributed to this section\n"
        f"   - order: Sequential position (1-based)\n"
        f"3. Sections should be ordered logically: background -> methodology -> "
        f"findings -> analysis -> implications -> future directions.\n"
        f"4. Each section should draw from at least 2 different perspectives "
        f"where possible (multi-perspective synthesis).\n"
        f"5. Do NOT create a section for every single Q&A pair — cluster "
        f"related information together.\n"
        f"6. Ensure COMPLETE COVERAGE: every key finding from the interviews "
        f"should appear in at least one section's evidence_summary.\n\n"
        f"Return a JSON object with a 'sections' array of outline section objects."
    )

    try:
        result = await client.generate_structured(
            prompt=prompt,
            schema=StormOutlinePlan,
            max_tokens=PG_STORM_OUTLINE_MAX_TOKENS,
            reasoning_enabled=True,
        )

        if result and result.sections:
            logger.info(
                "[STORM] Phase 3: Generated outline with %d sections from %d conversations",
                len(result.sections),
                len(conversations),
            )
            for section in result.sections:
                logger.debug(
                    "[STORM]   [%d] %s (%d perspectives)",
                    section.order,
                    section.title[:60],
                    len(section.perspectives),
                )
            return [s.model_dump() for s in result.sections]

    except Exception as exc:
        logger.warning(
            "[STORM] Phase 3: Outline generation failed: %s — using fallback",
            str(exc)[:200],
        )

    # Fallback: one section per perspective
    return _fallback_outline(conversations, query)


def _fallback_outline(
    conversations: list[StormConversation],
    query: str,
) -> list[dict]:
    """Generate a simple fallback outline when LLM outline generation fails.

    Creates one section per perspective that had conversations, using the
    conversation key findings as evidence summaries. Functional but less
    nuanced than LLM-generated outlines.
    """
    sections = []
    for i, conv in enumerate(conversations, 1):
        # Collect all key findings from this perspective's conversations
        findings = []
        for rnd in conv.rounds:
            findings.extend(rnd.get("key_findings", []))

        evidence = "; ".join(f[:100] for f in findings[:8])

        sections.append(StormOutlineSection(
            title=f"{conv.perspective.replace('_', ' ')} Perspective",
            description=(
                f"Analysis of {query[:100]} from the "
                f"{conv.perspective.lower().replace('_', ' ')} perspective, "
                f"based on {len(conv.rounds)} interview rounds."
            ),
            evidence_summary=evidence or "No specific findings extracted.",
            perspectives=[conv.perspective],
            order=i,
        ).model_dump())

    logger.info(
        "[STORM] Phase 3 fallback: Generated %d sections (1 per perspective)",
        len(sections),
    )
    return sections


