def _import_search_tools():
    """Import search tools from existing infrastructure."""
    from src.polaris_graph.agents.searcher import _run_web_searches, _import_search_tools as _get_tools

    return _run_web_searches, _get_tools


async def _search_for_question(
    question_queries: list[str],
    region: str,
    perspective: str = "",
) -> list[dict]:
    """Execute web searches for a STORM interview question.

    Reuses the battle-tested _run_web_searches from searcher.py with
    a concurrency-limited semaphore. Returns raw search result dicts.
    """
    if not question_queries:
        return []

    try:
        from src.polaris_graph.agents.searcher import _run_web_searches, _import_search_tools

        web_search_fn, _ = _import_search_tools()
        results = await _run_web_searches(web_search_fn, question_queries, region)

        # Tag results as originating from STORM interviews
        for r in results:
            r["storm_interview"] = True
            if perspective:
                r["perspective_source"] = perspective

        return results

    except Exception as exc:
        logger.warning(
            "[STORM] Search failed for queries %s: %s",
            [q[:40] for q in question_queries[:3]],
            str(exc)[:200],
        )
        return []


